// Word history tracking for preventing repetition across games
interface WordHistory {
  usedWords: string[][];  // Array of word sets from previous games
  maxGames: number;       // Maximum games to track (20)
}

let wordHistory: WordHistory = {
  usedWords: [],
  maxGames: 20
};

export function getRandomWords(count: number): string[] {
  // Collect all words used in the last 20 games (or fewer if less have been played)
  const recentlyUsedWords = new Set<string>();
  wordHistory.usedWords.forEach(gameWords => {
    gameWords.forEach(word => recentlyUsedWords.add(word));
  });

  // Filter out recently used words
  const availableWords = WORD_BANK.filter(word => !recentlyUsedWords.has(word));

  // If we don't have enough available words (shouldn't happen with 600+ words and 25 per game),
  // fall back to using all words
  const wordsToUse = availableWords.length >= count ? availableWords : WORD_BANK;

  // Shuffle and select words
  const shuffled = [...wordsToUse].sort(() => Math.random() - 0.5);
  const selectedWords = shuffled.slice(0, count);

  // Add to history and maintain max size
  wordHistory.usedWords.push(selectedWords);
  if (wordHistory.usedWords.length > wordHistory.maxGames) {
    wordHistory.usedWords.shift(); // Remove oldest game
  }

  return selectedWords;
}

// Function to reset word history (useful for new sessions)
export function resetWordHistory(): void {
  wordHistory = {
    usedWords: [],
    maxGames: 20
  };
}
